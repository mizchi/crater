#!/usr/bin/env node
//
// Generate an embedded reflow-VRT corpus from self-contained real-world
// fixtures. The js-target reflow-VRT harness (browser/shell/reflow_vrt_wbtest.mbt)
// can't read files at test time, so this bakes each fixture — plus a deterministic
// synthetic DOM mutation — into a generated MoonBit source the test consumes.
//
// Each fixture yields two (before, after) pairs:
//   - "prepend": insert a probe block at the START of <body> (shifts everything
//     below — stresses flow-shift reconcile across a real, deep page)
//   - "append": insert a probe block at the END of <body> (preceding layout is
//     unchanged — exercises the block-flow memo reuse path on a real page)
//
// Run: node scripts/gen-reflow-vrt-fixtures.mjs
// Output: browser/shell/reflow_vrt_fixtures.mbt  (committed; regenerate on change)
import fs from "node:fs"
import path from "node:path"

const repo_root = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..")

// Self-contained fixtures only (inline <style>, no external css/js). mdn-grid is
// intentionally omitted: at ~370 KB it would be an unwieldy string literal.
const FIXTURES = [
  { name: "example-com", file: "real-world/example-com/index.html" },
  { name: "hacker-news", file: "real-world/hacker-news/index.html" },
]

const PROBE = '<div data-reflow-probe style="width:120px;height:44px">reflow probe</div>'

function insert_at_body_start(html) {
  // After the first <body ...> open tag.
  const m = /<body\b[^>]*>/i.exec(html)
  if (!m) return null
  const at = m.index + m[0].length
  return html.slice(0, at) + PROBE + html.slice(at)
}

function insert_at_body_end(html) {
  const i = html.toLowerCase().lastIndexOf("</body>")
  if (i < 0) return null
  return html.slice(0, i) + PROBE + html.slice(i)
}

// Escape a string for a MoonBit double-quoted literal. Keep raw UTF-8 bytes
// (MoonBit source is UTF-8); only escape what the literal syntax requires.
function mbt_escape(s) {
  let out = ""
  for (const ch of s) {
    const code = ch.codePointAt(0)
    if (ch === "\\") out += "\\\\"
    else if (ch === '"') out += '\\"'
    else if (ch === "\n") out += "\\n"
    else if (ch === "\r") out += "\\r"
    else if (ch === "\t") out += "\\t"
    else if (code < 0x20) out += "\\u{" + code.toString(16) + "}"
    else out += ch
  }
  return out
}

const entries = []
for (const fx of FIXTURES) {
  const html = fs.readFileSync(path.join(repo_root, fx.file), "utf8")
  const prepend = insert_at_body_start(html)
  const append = insert_at_body_end(html)
  if (prepend) entries.push({ name: `${fx.name}: probe at body start (flow shift)`, before: html, after: prepend })
  if (append) entries.push({ name: `${fx.name}: probe at body end (memo reuse)`, before: html, after: append })
}

let body = ""
body += "// GENERATED by scripts/gen-reflow-vrt-fixtures.mjs — DO NOT EDIT.\n"
body += "// Real-world fixture pages + a deterministic synthetic mutation, embedded\n"
body += "// for the js-target reflow-VRT harness (see reflow_vrt_wbtest.mbt).\n\n"
body += "///|\n"
body += "/// (name, before, after) corpus derived from real-world fixtures.\n"
body += "fn reflow_vrt_fixture_corpus() -> Array[(String, String, String)] {\n"
body += "  [\n"
for (const e of entries) {
  body += `    (\n`
  body += `      "${mbt_escape(e.name)}",\n`
  body += `      "${mbt_escape(e.before)}",\n`
  body += `      "${mbt_escape(e.after)}",\n`
  body += `    ),\n`
}
body += "  ]\n"
body += "}\n"

const out_path = path.join(repo_root, "browser/shell/reflow_vrt_fixtures.mbt")
fs.writeFileSync(out_path, body)
console.error(`[gen-reflow-vrt-fixtures] wrote ${entries.length} entries to ${out_path}`)
